package com.alibaba.csp.switchcenter.bean;

public class ValueConfig {

    private String value;

    private String des;

    public ValueConfig(String value, String des) {
        this.value = value;
        this.des = des;
    }

    public ValueConfig() {

    }

    public ValueConfig(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

    @Override
    public String toString() {
        return "ValueConfig [value=" + value + ", des=" + des + "]";
    }

}
